string x;
map<string, bool> M;

int check(int sp, int len)
{
	string s1 = x.substr(sp, len);
	string s2 = s1; reverse(all(s2));

	if(s1==s2 && M.find(s1)==M.end()){
		M[s1] = true;
		return 1;
	}

	return 0;
}


